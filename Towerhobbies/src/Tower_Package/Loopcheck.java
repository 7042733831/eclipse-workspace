package Tower_Package;

public class Loopcheck {
	
	public static void main( String args[])
	{
		for(int i=2;i<50;i++)
		{
			System.out.print(i);
			System.out.println("\n");
			i=i+1;
		}
		
		for(int i=1;i<=50;i++)
		{
			System.out.println(i);
			i=i+1;
			System.out.println("\n");
			
		}
	}
          
}

